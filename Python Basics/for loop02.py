# for loop

for i in range(10):
    print(i)
    

for i in range(10):
    if i==5:             # For skip any number
        continue      
    print(i)

for i in range(10):
    print(i)             # For break on any number
    if i==7:
        break

for x in "MILAN PATEL":
    print(x)

for x in range(1,25,3):  # 1 to 25 by increasing 3 
    print(x)      





